# Password Modifier
users_password = input()  # User is prompted to input password
users_password = users_password.replace('i', '!')  # i is replaced with !
users_password = users_password.replace('a', '@')  # a is replaced with @
users_password = users_password.replace('m', 'M')  # m is replaced with M
users_password = users_password.replace('o', '.')  # o is replaced with .
users_password = users_password.replace('B', '8')  # B is replaced with 8
print(users_password + 'q*s')  # q*s is added next to the string value

